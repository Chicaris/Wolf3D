int		ft_isalnum(int c)
{
	int res;

	res = 0;
	if (c >= 48 && c <= 57)
		res = 1;
	if (c >= 65 && c <= 90)
		res = 1;
	if (c >= 97 && c <= 122)
		res = 1;
	return (res);
}
