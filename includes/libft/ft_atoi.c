#include "libft.h"

int		ft_atoi(const char *str)
{
	int		sign;
	long	res;

	while (*str == ' ' || *str == '\t' || *str == '\n' || *str == '\v'
			|| *str == '\r' || *str == '\f')
		str++;
	res = 0;
	sign = 1;
	if (*str == '-' || *str == '+')
	{
		sign = (*str == '-' ? -1 : 1);
		str++;
	}
	while (ft_isdigit(*str))
	{
		if (res > 999999999999999999 / 10)
			return (res = (sign == 1 ? -1 : 0));
		res = (res * 10) + (*str - '0');
		str++;
	}
	return (res * sign);
}
