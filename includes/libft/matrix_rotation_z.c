#include "libft.h"

void	get_rotate_z(float dst[4][4], float angle)
{
	matrix_unit(dst);
	dst[0][0] = cos(angle);
	dst[1][1] = dst[0][0];
	dst[1][0] = sin(angle);
	dst[0][1] = -dst[1][0];
}
