void		matrix_multi(float dest[4][4], float a[4][4], float b[4][4])
{
	int		i;
	int		j;
	int		k;

	i = 0;
	while (i < 4)
	{
		j = 0;
		while (j < 4)
		{
			dest[i][j] = 0.0;
			k = 0;
			while (k < 4)
			{
				dest[i][j] += a[i][k] * b[k][j];
				k++;
			}
			j++;
		}
		i++;
	}
}
