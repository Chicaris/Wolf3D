void					matrix_transpose(float dest[4][4], float src[4][4])
{
	int		i;
	int		j;

	i = 0;
	while (i <= 3)
	{
		j = 0;
		while (j <= 3)
		{
			dest[i][j] = src[j][i];
			j++;
		}
		i++;
	}
}
